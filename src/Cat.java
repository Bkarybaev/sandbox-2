public class Cat {
    String name;
    String color;
    int age;

    public Cat(String name,int age){
        this.age = age;
        this.name = name;
    };

    public Cat(String color,String name){
        this.color = color;
        this.name = name;
    }

    public Cat(int age){
        this.age = age ;
    }

}
